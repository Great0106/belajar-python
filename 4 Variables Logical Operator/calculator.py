operator = input("Operator: ")
if operator == "+":
  number1 = int(input("Input the first number."))
  number2 = int(input("Input the second number."))
  result = number1 + number2
  print(result)
elif operator == "-":
  number1 = int(input("Input the first number."))
  number2 = int(input("Input the second number."))
  result = number1 - number2
  print(result)
elif operator == "*":
  number1 = int(input("Input the first number."))
  number2 = int(input("Input the second number."))
  result = number1 * number2
  print(result)
elif operator == "/":
  number1 = int(input("Input the first number."))
  number2 = int(input("Input the second number."))
  result = number1 / number2
  print(result)